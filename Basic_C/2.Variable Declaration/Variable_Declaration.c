#include<stdio.h>

int main(int argc, char **argv) {

    //Syntax of Variable declarations is:
    /*
        data_type variable_name;
        
        or 

        data_type variable_name=<value>;


    */


   //integer type 
   int a=1;
   //char type
   char b='b';
   //float type
   float d=1.0f; /*or*/ float e=1.1;
   //double type
   double f=1.222222222222334;


   return 0;
   
}
